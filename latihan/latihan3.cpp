#include <iostream>

using namespace std;

int main(){
    float a;
    float b;

    cout<<"Masukkan Angka Pertama : ";
    cin>>a;
    cout<<"Masukkan Angka Kedua   : ";
    cin>>b;

    cout<<"Penjumlahan : "<<a+b<<endl;
    cout<<"Pengurangan : "<<a-b<<endl;
    cout<<"Perkalian   : "<<a*b<<endl;
    cout<<"Pembagian   : "<<a/b<<endl;
    return 0;
}